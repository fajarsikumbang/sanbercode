// LOOPING PERTAMA
console.log("LOOPING PERTAMA");
let i = 2;
while (i <= 20) {
  console.log(i + " - I love coding");
  i += 2; // Menambah i dengan 2 setiap iterasi
}

// LOOPING KEDUA
console.log("LOOPING KEDUA");
i = 20;
while (i >= 2) {
  console.log(i + " - I will become a mobile developer");
  i -= 2; // Mengurangi i dengan 2 setiap iterasi
}
